import re


def split_tspan(line, char_width=10):
    # extract x, y, and text
    m = re.search(r'x="(\d+)"\s+y="(\d+)">(.*)</tspan>', line)
    if not m:
        raise ValueError("Invalid tspan input")

    base_x = int(m.group(1))
    y = m.group(2)
    text = m.group(3)

    result = []
    buffer = ""
    index = 0  # character index in original string

    for ch in text:
        if ch == "•":
            if buffer:
                x = base_x + (index - len(buffer)) * char_width
                result.append(f'<tspan x="{x}" y="{y}">{buffer}</tspan>')
                buffer = ""
        else:
            buffer += ch
        index += 1

    # flush remaining buffer
    if buffer:
        x = base_x + (index - len(buffer)) * char_width
        result.append(f'<tspan x="{x}" y="{y}">{buffer}</tspan>')

    return "".join(result)


